export function IconoCorazon(props) {
  const [activo, setActivo] = useState(props.esFavorito);

  function animacionCorazon() {
    setActivo(true);

    setTimeout(() => {
      setActivo(false);
    }, 500);
  }

  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={props.ancho}
      height={props.alto}
      viewBox="0 0 24 24"
      fill={`${props.esFavorito ? 'whitesmoke' : 'none'}`}
      stroke="currentColor"
      strokeWidth="2"
      strokeLinecap="round"
      strokeLinejoin="round"
      className={`feather feather-heart ${activo ? 'active' : ''}`}
      onClick={animacionCorazon}
    >
      <path d="M20.84 4.61a5.5 5.5 0 0 0-7.78 0L12 5.67l-1.06-1.06a5.5 5.5 0 0 0-7.78 7.78l1.06 1.06L12 21.23l7.78-7.78 1.06-1.06a5.5 5.5 0 0 0 0-7.78z" />
    </svg>
  );
}

